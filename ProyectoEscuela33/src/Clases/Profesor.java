package Clases;

/**
 *
 * @author FRANZ
 */
public class Profesor extends Persona{
    String idProfesor;
    Persona persona;

    public Profesor() {
    }

    public Profesor(String idProfesor, Persona persona) {
        this.idProfesor = idProfesor;
        this.persona = persona;
    }

    public Profesor(String idProfesor, Persona persona, String nombre, String apellido, String cedula, String telefono, String direccion, String correo, int edad) {
        super(nombre, apellido, cedula, telefono, direccion, correo, edad);
        this.idProfesor = idProfesor;
        this.persona = persona;
    }

    public String getIdProfesor() {
        return idProfesor;
    }

    public void setIdProfesor(String idProfesor) {
        this.idProfesor = idProfesor;
    }

    public Persona getPersona() {
        return persona;
    }

    public void setPersona(Persona persona) {
        this.persona = persona;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getApellido() {
        return apellido;
    }

    public void setApellido(String apellido) {
        this.apellido = apellido;
    }

    public String getCedula() {
        return cedula;
    }

    public void setCedula(String cedula) {
        this.cedula = cedula;
    }

    public String getDireccion() {
        return direccion;
    }

    public void setDireccion(String direccion) {
        this.direccion = direccion;
    }

    public String getCorreo() {
        return correo;
    }

    public void setCorreo(String correo) {
        this.correo = correo;
    }

    public int getEdad() {
        return edad;
    }

    public void setEdad(int edad) {
        this.edad = edad;
    }
    
    
    
}
