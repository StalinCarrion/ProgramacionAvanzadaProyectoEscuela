    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed

        ObjRepresentante.setCedula((this.jTextField7.getText().replaceAll("", "")));

        try {
            if (jTextField7.getText().length() == 0 || " ".equals(jTextField7.getText())) {
                JOptionPane.showMessageDialog(rootPane, "Ingrese un número de cédula.");
            } else {
                ObjRepresentante = ObjLogRep.BuscarRepresentante(ObjRepresentante);
                if (ObjRepresentante.getCedula().equals(this.jTextField7.getText())) {
                    JOptionPane.showMessageDialog(rootPane, "El representante es: " + ObjRepresentante.getNombre() + " " + ObjRepresentante.getApellido());
                    this.jTextField4.setText(String.valueOf(ObjRepresentante.getIdRepresentante()));
                    this.jTextField1.setEnabled(true);
                    this.jTextField2.setEnabled(true);
                    this.jTextField3.setEnabled(true);
                    this.jTextField5.setEnabled(true);
                    this.jTextField6.setEnabled(true);
                } else {
                    JOptionPane.showMessageDialog(rootPane, "El representante no está registrado.");
                    dispose();
                    ObjCrearRep.setVisible(true);
                    ObjCrearRep.jTextField10.setText(this.jTextField7.getText());
                }
            }

        } catch (Exception e) {
            System.err.println(e);
            JOptionPane.showMessageDialog(rootPane, "El representante no está registrado.");
            dispose();
            ObjCrearRep.setVisible(true);
            ObjCrearRep.jTextField10.setText(this.jTextField7.getText());
        }
    }//GEN-LAST:event_jButton8ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if (this.jTextField1.getText().isEmpty() || this.jTextField2.getText().isEmpty() || this.jTextField5.getText().isEmpty()
                || this.jTextField6.getText().isEmpty() || this.jTextField3.getText().isEmpty()) {
            int contador = 0;
            String In = "";
            String CamposVacios = "";
            if (this.jTextField1.getText().isEmpty()) {
                CamposVacios += "\n - Nombre";
                contador++;
            }
            if (this.jTextField2.getText().isEmpty()) {
                CamposVacios += "\n - Apellido";
                contador++;
            }
            if (this.jTextField5.getText().isEmpty()) {
                CamposVacios += "\n - Cédula";
                contador++;
            }
            if (this.jTextField6.getText().isEmpty()) {
                CamposVacios += "\n - Dirección";
                contador++;
            }
            if (this.jTextField3.getText().isEmpty()) {
                CamposVacios += "\n - Edad";
                contador++;
            }

            if (contador > 1) {
                In = ("Campos vacíos: " + CamposVacios);
            } else {
                In = "Campo vacío: " + CamposVacios;
            }

            JOptionPane.showMessageDialog(rootPane, In);
        } else {
            ObjEstudiante.setNombre(this.jTextField1.getText());
            ObjEstudiante.setApellido(this.jTextField2.getText());
            ObjEstudiante.setCedula(this.jTextField5.getText());
            ObjEstudiante.setDireccion(this.jTextField6.getText());
            ObjEstudiante.setEdad(Integer.parseInt(this.jTextField3.getText()));

            ObjRepresentante.setCedula(this.jTextField7.getText());

            ObjRepresentante = ObjLogRep.BuscarRepresentante(ObjRepresentante);

            ObjEstudiante.setRepresentante(ObjRepresentante);

            if (ObjLogEst.InsertarEstudiante(ObjEstudiante)) {

                LogicaParalelo ObjLP = new LogicaParalelo();

                ObjMat.setFecha(getFechaActual());
                ObjMat.setNumMatricula(1);

                ObjEstudiante.setCedula(this.jTextField5.getText());
                ObjMat.setEstudiante(ObjEstudiante);

                ObjGradoParalelo.setParalelo(this.jComboBox3.getSelectedItem().toString());

                ObjGradoParalelo = ObjLP.BuscarParaleloGrado(ObjGradoParalelo.getGrado(), ObjGradoParalelo.getParalelo());

                ObjOA.setIdGradoParalelo(ObjGradoParalelo);

                ObjOA = ObjLOA.BuscarIdOfertaAcademica(ObjOA);

                System.out.println(ObjOA.getIdOfertaAcademica());

                ObjMat.setOfertaAcademica(ObjOA);

                if (objLogicaGP.MatricularEstudiante(ObjMat)) {
                    JOptionPane.showMessageDialog(rootPane, "Estudiante matriculado correctamente");
                    this.imprimirPDF();
                    //Mostrar Oferta Académica
                    ObjGradoParalelo.setGrado(Integer.parseInt(this.jComboBox2.getSelectedItem().toString()));
                    ObjGradoParalelo.setParalelo(this.jComboBox3.getSelectedItem().toString());
                    ListarMateriasAgregadas();

                } else {
                    JOptionPane.showMessageDialog(rootPane, "Error");

                }
            } else {
                JOptionPane.showMessageDialog(rootPane, "Los datos del estudiante no están llenados correctamente");
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

