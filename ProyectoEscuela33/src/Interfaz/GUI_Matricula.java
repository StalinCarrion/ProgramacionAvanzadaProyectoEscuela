    public void ListarMateriasAgregadas() {
        ArrayList<Materia> arrayListMaterias = new ArrayList<Materia>();
        LogicaOfertaAcademica objLogicaOfertaAcademica = new LogicaOfertaAcademica();

        arrayListMaterias = objLogicaOfertaAcademica.BuscarOfetaAcademica(ObjGradoParalelo);
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("id");
        modelo.addColumn("Nombre");
        modelo.addColumn("Horas");

        for (Materia objMateria : arrayListMaterias) {
            Object[] fila = new Object[3];
            fila[0] = objMateria.getIdMateria();
            fila[1] = objMateria.getNombreMateria();
            fila[2] = objMateria.getHoras();
            modelo.addRow(fila);
        }
        this.jTable1.setModel(modelo);

    }
