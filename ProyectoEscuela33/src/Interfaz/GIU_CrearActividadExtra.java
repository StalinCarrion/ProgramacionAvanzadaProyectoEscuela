    private void NombreActividadKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_NombreActividadKeyTyped
        // TODO add your handling code here:
        char validar = evt.getKeyChar();
        if (Character.isDigit(validar)) {
            getToolkit().beep();
            evt.consume();
        }
        if (Character.isLowerCase(validar)) { // SOLO MAYÚSCULAS
            String cad = ("" + validar).toUpperCase();
            validar = cad.charAt(0);
            evt.setKeyChar(validar);
        }

    }//GEN-LAST:event_NombreActividadKeyTyped

    private void BotonValidarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonValidarActionPerformed
        // TODO add your handling code here:
        ObjAct.setNombreActividad(this.NombreActividad.getText());
        if (ObjAct.getNombreActividad().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Introduzca un nombre a la actividad");
        } else {
            if (this.BuscarActividad(ObjAct)) {
                this.NombreActividad.setEnabled(false);
                this.jBotonCancelar.setEnabled(true);
                JOptionPane.showMessageDialog(rootPane, "¡PUEDES REGISTRAR ESTA ACTIVIDAD!");
                this.jComboBoxOpicionActividad.setEnabled(true);
                this.jBotonConfirmar.setEnabled(true);

            } else {

                JOptionPane.showMessageDialog(rootPane, "¡YA EXISTE ESTA ACTIVIDAD!");

            }
        }


    }//GEN-LAST:event_BotonValidarActionPerformed

