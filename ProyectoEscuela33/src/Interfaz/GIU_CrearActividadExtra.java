    private void jBotonCrearActividadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBotonCrearActividadActionPerformed
        // TODO add your handling code here:

        if (this.jTextAporteEconomico.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "El campo de aporte económico esta vacio");
        } else if (this.jComboBoxOpicionActividad.getSelectedItem().equals("Elija una opción")) {
            JOptionPane.showMessageDialog(null, "Seleccione un tipo de actividad");
        } else {
            try {
                Calendar fecha = new GregorianCalendar();

                int mesActual = fecha.get(Calendar.MONTH) + 1;
                int diaActual = fecha.get(Calendar.DAY_OF_MONTH);

                String diaInicio = Integer.toString(this.jFechaInicio.getCalendar().get(Calendar.DAY_OF_MONTH));
                String mesInicio = Integer.toString(this.jFechaInicio.getCalendar().get(Calendar.MONTH) + 1);
                String yearInicio = Integer.toString(this.jFechaInicio.getCalendar().get(Calendar.YEAR));
                String fechaInicio = (yearInicio + "-" + mesInicio + "-" + diaInicio);

                String diaFin = Integer.toString(this.jFechaFin.getCalendar().get(Calendar.DAY_OF_MONTH));
                String mesFin = Integer.toString(this.jFechaFin.getCalendar().get(Calendar.MONTH) + 1);
                String yearFin = Integer.toString(this.jFechaFin.getCalendar().get(Calendar.YEAR));

                if (Integer.parseInt(mesFin) >= Integer.parseInt(mesInicio) && Integer.parseInt(diaFin) >= Integer.parseInt(diaInicio) && Integer.parseInt(mesInicio) >= mesActual && Integer.parseInt(diaInicio) >= diaActual) {
                    String fechaFin = (yearFin + "-" + mesFin + "-" + diaFin);

                    ObjAct.setFechaInicio(Date.valueOf(fechaInicio));
                    ObjAct.setFechaFinal(Date.valueOf(fechaFin));
                    ObjAct.setTipoActividad(this.jComboBoxOpicionActividad.getSelectedItem().toString());
                    ObjAct.setNombreActividad(this.NombreActividad.getText().toUpperCase());
                    ObjAct.setAporteEconomico(Double.parseDouble(this.jTextAporteEconomico.getText()));
                    if (ObjLogAct.InsertarActividades(ObjAct)) {
                        JOptionPane.showMessageDialog(null, "Se registró correctamente en la base de datos");
                        this.NombreActividad.setText("");
                        this.jComboBoxOpicionActividad.setSelectedIndex(0);
                        this.jBotonCrearActividad.setEnabled(false);
                        this.NombreActividad.setEnabled(true);
                        this.jTextAporteEconomico.setText("");
                        this.jTextAporteEconomico.setEnabled(false);
                        this.jRadioButtonConAporteEconómico.setEnabled(false);
                        this.jRadioButtonSinAporteEconómico.setEnabled(false);
                        this.jComboBoxOpicionActividad.setEnabled(false);
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Las fechas introducidas son incorrectas, debe seleccionar fechas igual o superiores a la de hoy.");
                }
            } catch (NullPointerException x) {
                JOptionPane.showMessageDialog(null, "Introduzca las fechas de inicio y fin de la actividad");
            }

        }

    }//GEN-LAST:event_jBotonCrearActividadActionPerformed

