    private void jBEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBEliminarActionPerformed

        int row = this.jTableMateriasAgregadas.getSelectedRow();
        String gradoParalelo = (String) this.jCBGrado.getSelectedItem();
        String materia = this.jTableMateriasAgregadas.getValueAt(row, 0).toString();
        objMateria.setIdMateria(Integer.parseInt(materia));
        objMateria.getIdMateria();
        objGradoParalelo.setIdGradoParalelo(Integer.parseInt(gradoParalelo));
        objGradoParalelo.setGrado(Integer.parseInt(gradoParalelo));
        objGradoParalelo.getGrado();

        if (JOptionPane.showConfirmDialog(null, "¿Está seguro de eliminar la materia?", "Advertencia", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE) == 0) {
            try {
                objLogicaOfertaAcademica.EliminarAlumnoActividad(objMateria, objGradoParalelo);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, "La materia no se puede eliminar");
            }finally{
                JOptionPane.showMessageDialog(rootPane, "La materia no se puede eliminar porque hay estudiantes matriculados");
            }
        }

    }//GEN-LAST:event_jBEliminarActionPerformed

