    public void ListarMaterias() {
        ArrayList<Materia> ArrayMaterias = new ArrayList<Materia>();
        ArrayMaterias = objLogicaMateria.ListarMaterias();
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("idMateria");
        modelo.addColumn("Nombre");
        modelo.addColumn("Horas");

        for (Materia objMateria : ArrayMaterias) {
            Object[] fila = new Object[3];
            fila[0] = objMateria.getIdMateria();
            fila[1] = objMateria.getNombreMateria();
            fila[2] = objMateria.getHoras();
            modelo.addRow(fila);
        }
        this.jTableMaterias.setModel(modelo);

    }

    public void LlenarTablaMaterias() {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Nombre");
        modelo.addColumn("Horas");
        modelo.addColumn("Grado");
        modelo.addColumn("Paralelo");
        modelo.addColumn("Profesor");
        for (Materia arrayListMateria : arrayListMaterias) {
            Object[] fila = new Object[5];
            fila[0] = arrayListMateria.getNombreMateria();
            fila[1] = arrayListMateria.getHoras();
            fila[2] = arrayListMateria.getGradoparalelo().getGrado();
            fila[3] = arrayListMateria.getGradoparalelo().getParalelo();
            fila[4] = arrayListMateria.getProfesor().getNombre();

            modelo.addRow(fila);
        }
        this.jTableMateriasAgregadas.setModel(modelo);
    }
    public void CargarParalelos(GradoParalelo GradoSelect) {
        ArrayList<GradoParalelo> ArrayPar = new ArrayList<GradoParalelo>();
        GradoParalelo objGP = new GradoParalelo();

        try {
            ArrayPar = objLogicaGP.ObtenerParalelos(GradoSelect);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "error");
            System.err.println(e);
        }

        DefaultComboBoxModel dlm = new DefaultComboBoxModel();

        for (GradoParalelo c : ArrayPar) {
            dlm.addElement(c.getParalelo());
        }
        this.jComboBox1.setModel(dlm);
    }

