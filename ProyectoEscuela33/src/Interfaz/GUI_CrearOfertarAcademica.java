    private void jbtnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnAgregarActionPerformed
        // TODO add your handling code here:
        String gradoParalelo = (String) this.jCBGrado.getSelectedItem();
        int row = this.jTableMaterias.getSelectedRow();
        String materia = this.jTableMaterias.getValueAt(row, 0).toString();
        objGradoParalelo.setIdGradoParalelo(Integer.parseInt(gradoParalelo));
        objMateria.setIdMateria(Integer.parseInt(materia));
        objMateria.getIdMateria();
        objGradoParalelo.setGrado(Integer.parseInt(gradoParalelo));
        objGradoParalelo.getGrado();
        if (objLogicaOfertaAcademica.BuscarIdMateria(objMateria, objGradoParalelo)) {
            JOptionPane.showMessageDialog(rootPane, "Ya esta registrado");
        } else {
            JOptionPane.showMessageDialog(rootPane, "Correcto");
            objLogicaOfertaAcademica.InsertarOfertaAcademica(objGradoParalelo, objMateria);
        }
        ListarMateriasAgregadas();
    }//GEN-LAST:event_jbtnAgregarActionPerformed

